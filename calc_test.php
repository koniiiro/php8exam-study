<h1>演算子の優先順位</h1>
<p>

1. 計算のステップ<br>
ステップ①：$a の計算<br>
$a = 2 + 3 * 6;
算数のルールと同じく、足し算よりも掛け算が優先されます。<br>

3 * 6 = 18<br>

2 + 18 = 20<br>
よって、$a は 20 です。<br><br>

ステップ②：$b の計算<br>
$b = $a % 7;<br>
% は「割り算の余り」を求める演算子（剰余演算子）です。<br>

20 ÷ 7 = 2 余り 6<br>
よって、$b は 6 です。<br><br>

ステップ③：$c の計算<br>
$c = $b ** 2;<br>
** は「べき乗（〜乗）」を求める演算子です。<br>

6 の 2乗（6 × 6） = 36<br>
よって、$c は 36 です。<br><br>

2. 試験で狙われる演算子のポイント<br>
%（剰余）: 割り切れた場合は 0 になるという点も覚えておきましょう。<br>

**（べき乗）: PHP 5.6で追加された比較的新しい演算子なので、試験によく出ます。<br>

優先順位:<br>
<br>
**（べき乗）が最強

*（掛け算）、/（割り算）、%（余り）<br>

+（足し算）、-（引き算）<br><br>

</p>
    <?php
$a = 2 + 3 * 6; // 20
echo "aの値: " . $a . "\n"; 

$b = $a % 7;    // 20 / 7 の余りは 6
echo "bの値: " . $b . "\n";

$c = $b ** 2;   // 6の2乗は 36
echo "cの値: " . $c . "\n";

print($c);      // 最終結果: 36
?>

<p>
    もし本番で「計算順序が不安だな」と思ったら、<br>
    **「掛け算・割り算・余りは、足し算・引き算より強い！」**<br>
    と心の中で唱えてください。<br>
    
