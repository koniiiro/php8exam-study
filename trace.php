変数の値が上書きされていく過程と、代入されたタイミングでの値の保持を理解しているかを問う問題です。<br>
特に「途中で変数 $a に代入したあと、元の変数の値が変わったら $a も変わるのか？」という点がポイントになります。<br>
1. 1行ずつ値を追いかける「トレース」計算の過程を整理しましょう。<br><br>
$n = 2; $p = 3;<br>
++$n; (1増やす)<br>
$p *= 2; (2倍にする)<br>
$a = "$n, $p";<br>
--$n; (1減らす)<br>
$p *= 3; (3倍にする)<br>
$b = "$n, $p";<br>

2. 試験で狙われる「ひっかけ」ポイント<br>
値のコピー: $a = "$n, $p"; と代入した時点で、その時の $n と $p の中身が「文字列」として $a にコピーされます。<br>

その後に $n や $p の値を書き換えても、すでに代入済みの $a の中身は変わりません。<br>
（参照渡しという特殊な書き方をしない限り、PHPの代入は値のコピーです）<br><br>
    </p>

    <?php
$n = 2;
$p = 3;

++$n;   // $n は 3
$p *= 2; // $p は 6
$a = "$n, $p"; // この時点で $a は "3, 6"

--$n;   // $n は 2 に戻る
$p *= 3; // $p は 18 に増える
$b = "$n, $p"; // この時点で $b は "2, 18"

// 連結演算子 (.) でつないで表示
print $a . ' / ' . $b; 
// 結果: 3, 6 / 2, 18
?>
    
